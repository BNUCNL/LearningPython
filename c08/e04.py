'''
问：举出四种在原处修改字典对象的运算。
答：这里给出四种：1.对旧键的赋值；2.对新键的赋值；3.用del语句删除一个键的元素；4.用update()方法以盲目覆盖相同键的值的方式（如果有相同键的话）“合并”两个字典。
'''

#代码验证：
D={'a':0, 'c':3}
print("字典的初始值", D)

D['c']=2
print("经D['c']=2修改后：", D,"  旧键赋值")

D['b']=1
print("经D['b']=1修改后：", D,"  新键赋值")

del D['c']
print("经del D['c']修改后：", D,"  删除键的元素")

D.update({'d':3})
print("经D.update({'d':3})修改后：", D,"  update()方法")
